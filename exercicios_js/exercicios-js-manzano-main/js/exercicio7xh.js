function executarExercicio7xh() {
    alert(`Exercicio 7 H)\nElaborar um programa que calcule e apresente o volume de uma caixa retangular, por meio da fórmula VOLUME = COMPRIMENTO * LARGURA * ALTURA`)

    let comprimento = parseInt(prompt(`Digite o valor do comprimento: `))
    let largura = parseInt(prompt(`Digite o valor da largura: `))
    let altura = parseInt(prompt(`Digite o valor do comprimento: `))
    let volume = comprimento * largura * altura

    alert(`O volume da caiza será: ${volume}`)
}